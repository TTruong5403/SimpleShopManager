using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Design;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SimpleManager.Infrastructure
{
    public class DataContextFactory : IDesignTimeDbContextFactory<Data>
    {
        public Data CreateDbContext(string[] args)
        {
            var optionsBuilder = new DbContextOptionsBuilder<Data>();

            optionsBuilder.UseSqlServer(
                "Server=ADMIN;Database=SimpleShopManager;Trusted_Connection=True;TrustServerCertificate=True"
            );

            return new Data(optionsBuilder.Options);
        }
    }
}
